    sub = p.add_subparsers(dest="cmd", required=True)

    g = sub.add_parser("generate", help="regenerate the meta-custom-apps layer")
    g.add_argument("--apps-dir", required=True)
    g.add_argument("--layer-dir", required=True)
    g.add_argument("--default-image", default="adi-sc5xx-custom")
