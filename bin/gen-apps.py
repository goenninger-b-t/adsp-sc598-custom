#!/usr/bin/env python3
# gen-apps.py - Generate the meta-custom-apps Yocto layer from src/apps/.
#
# Each src/apps/<name>/ directory must contain an app.yaml manifest. This tool
# reads those manifests, validates them, and emits a complete Yocto layer
# consisting of:
#   - conf/layer.conf
#   - recipes-apps/<name>/<name>_<version>.bb (one per app)
#   - recipes-apps/<name>/files/...           (staged source/config/binary)
#   - recipes-core/packagegroups/packagegroup-custom-apps.bb
#   - recipes-core/images/<default-image>.bb  (inherits adsp-sc5xx-minimal-mmc)
#
# Subcommands:
#   generate   regenerate the meta-custom-apps layer from src/apps/
#   scaffold   create a new app skeleton under src/apps/<name>/
#   list       print configured apps
#
# See src/apps/hello-world/app.yaml for a full manifest example.

import argparse
import hashlib
import os
import re
import shutil
import sys
import textwrap
from pathlib import Path

try:
    import yaml
except ImportError:
    sys.stderr.write(
        "ERROR: PyYAML required.\n"
        "  Debian/Ubuntu: sudo apt-get install python3-yaml\n"
        "  pip:           pip3 install --user pyyaml\n"
    )
    sys.exit(1)


# ----------------------------------------------------------------- constants --

NAME_RE = re.compile(r"^[a-z][a-z0-9-]*$")
VERSION_RE = re.compile(r"^[A-Za-z0-9.+~_-]+$")
VALID_KINDS = ("local-source", "git", "prebuilt-binary", "prebuilt-tarball")
VALID_BUILD_SYSTEMS = ("cmake", "meson", "autotools", "make", "none")

# Inherit lines per build system.
INHERIT_FOR_BUILD = {
    "cmake": "cmake",
    "meson": "meson pkgconfig",
    "autotools": "autotools",
    "make": "",
    "none": "",
}

# Standard Yocto md5 for COMMON_LICENSE_DIR/MIT.
MIT_LICENSE_MD5 = "0835ade698e0bcf8506ecda2f7b4f302"

GENERATED_HEADER = (
    "# AUTO-GENERATED by bin/gen-apps.py - DO NOT EDIT.\n"
    "# Edit src/apps/<name>/app.yaml and run `make apps`.\n"
)


# ----------------------------------------------------------------- helpers ---

def die(msg):
    sys.stderr.write(f"gen-apps: ERROR: {msg}\n")
    sys.exit(1)


def info(msg):
    print(f"[gen-apps] {msg}")


def md5_of(path):
    h = hashlib.md5()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def sha256_of(path):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def write_if_changed(path, content):
    path = Path(path)
    if path.exists() and path.read_text() == content:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)
    return True


def copy_tree(src, dst):
    # Stage a source tree into the generated layer, stripping VCS/editor cruft
    # that has no business in the bitbake WORKDIR: the per-app .gitignore (it
    # only ignores the in-tree host build artifact), any stray .git/ metadata
    # or .gitattributes, and Vim swap files. Patterns match names at every
    # depth of the tree.
    dst = Path(dst)
    if dst.exists():
        shutil.rmtree(dst)
    shutil.copytree(src, dst, ignore=shutil.ignore_patterns(
        ".gitignore", ".git", ".gitattributes", "*.swp"))


# ----------------------------------------------------------------- manifest --

def load_manifest(app_dir):
    yaml_path = app_dir / "app.yaml"
    if not yaml_path.is_file():
        die(f"{app_dir.name}: missing app.yaml")
    with open(yaml_path) as f:
        try:
            m = yaml.safe_load(f) or {}
        except yaml.YAMLError as e:
            die(f"{app_dir.name}: invalid YAML: {e}")
    if not isinstance(m, dict):
        die(f"{app_dir.name}: app.yaml must be a mapping")
    return m


def validate(m, app_dir):
    name = m.get("name", "")
    if not isinstance(name, str) or not NAME_RE.match(name):
        die(f"{app_dir.name}: 'name' must match {NAME_RE.pattern!r}")
    if name != app_dir.name:
        die(f"{app_dir.name}: 'name' ({name!r}) must equal directory name ({app_dir.name!r})")

    version = str(m.get("version", "")).strip()
    if not version or not VERSION_RE.match(version):
        die(f"{name}: 'version' required (matching {VERSION_RE.pattern!r})")
    m["version"] = version

    if not m.get("summary"):
        die(f"{name}: 'summary' required")

    if not m.get("license"):
        die(f"{name}: 'license' required (use 'CLOSED' for proprietary/internal)")

    kind = m.get("kind")
    if kind not in VALID_KINDS:
        die(f"{name}: 'kind' must be one of {VALID_KINDS}")

    build = m.get("build") or {}
    if kind in ("local-source", "git"):
        bs = build.get("system")
        if bs not in VALID_BUILD_SYSTEMS:
            die(f"{name}: 'build.system' must be one of {VALID_BUILD_SYSTEMS} for kind={kind}")
    m["build"] = build

    if kind == "local-source":
        src = app_dir / "src"
        if not src.is_dir():
            die(f"{name}: kind=local-source requires {src}/ to exist")

    if kind == "git":
        git = m.get("git") or {}
        if not git.get("url"):
            die(f"{name}: 'git.url' required for kind=git")
        if not git.get("rev"):
            die(f"{name}: 'git.rev' (SRCREV) required for kind=git")
        git.setdefault("branch", "main")
        m["git"] = git

    if kind in ("prebuilt-binary", "prebuilt-tarball"):
        binary = m.get("binary") or {}
        url = binary.get("url")
        if not url:
            die(f"{name}: 'binary.url' required for kind={kind}")
        if url.startswith("file://"):
            local = app_dir / url[len("file://"):]
            if not local.is_file():
                die(f"{name}: binary.url points to missing file: {local}")
            # Auto-fill sha256 for local files; user need not pre-compute.
            binary.setdefault("sha256", sha256_of(local))
        elif url.startswith(("http://", "https://", "ftp://")):
            if not binary.get("sha256"):
                die(f"{name}: 'binary.sha256' required for remote URL")
        else:
            die(f"{name}: 'binary.url' must use file:// http(s):// or ftp://")
        m["binary"] = binary

    # Resolve and validate license file (unless CLOSED).
    if m["license"] != "CLOSED":
        lic = None
        for cand in ("LICENSE", "LICENSE.txt", "COPYING", "COPYING.md"):
            p = app_dir / cand
            if p.is_file():
                lic = p
                break
        if lic is None:
            die(f"{name}: license={m['license']!r} but no LICENSE/COPYING in {app_dir}/")
        m["_license_file"] = lic

    # Validate referenced files exist now, fail fast.
    for cfg in (m.get("config") or []):
        if not (app_dir / cfg["src"]).is_file():
            die(f"{name}: config src missing: {cfg['src']}")
        cfg.setdefault("mode", "0644")
    for f in (m.get("files") or []):
        if not (app_dir / f["src"]).is_file():
            die(f"{name}: files src missing: {f['src']}")
        f.setdefault("mode", "0644")

    service = m.get("service") or {}
    if service.get("unit"):
        unit = app_dir / service["unit"]
        if not unit.is_file():
            die(f"{name}: service unit missing: {unit}")
        service.setdefault("enable", True)
        m["service"] = service

    install = m.get("install") or {}
    for key in ("bindir", "sbindir", "libdir"):
        install.setdefault(key, [])
    install.setdefault("custom", [])
    for c in install["custom"]:
        c.setdefault("mode", "0644")
        if "src" not in c or "dst" not in c:
            die(f"{name}: install.custom entries need 'src' and 'dst'")
    m["install"] = install

    return m


# ------------------------------------------------------------ rendering ------

def render_recipe(m):
    """Return the full body of the .bb file for an app manifest."""
    name = m["name"]
    kind = m["kind"]
    build = m["build"]
    bs = build.get("system", "none")
    license_ = m["license"]
    install = m["install"]
    config = m.get("config") or []
    files_extra = m.get("files") or []
    service = m.get("service") or {}
    service_unit = Path(service["unit"]).name if service.get("unit") else None

    out = [GENERATED_HEADER, f'SUMMARY = "{m["summary"]}"', f'LICENSE = "{license_}"']

    # LIC_FILES_CHKSUM. We stage LICENSE via SRC_URI below so it lands in
    # ${WORKDIR}; reference it absolutely because S may be a subdirectory.
    if license_ != "CLOSED":
        lic = m["_license_file"]
        out.append(f'LIC_FILES_CHKSUM = "file://${{WORKDIR}}/{lic.name};md5={md5_of(lic)}"')

    if m.get("depends"):
        out.append(f'DEPENDS = "{" ".join(m["depends"])}"')
    if m.get("rdepends"):
        out.append(f'RDEPENDS:${{PN}} = "{" ".join(m["rdepends"])}"')

    # ----- SRC_URI ---------------------------------------------------------
    src_uris = []
    if kind == "local-source":
        src_uris.append("file://src")
    elif kind == "git":
        git = m["git"]
        url = git["url"]
        rev = git["rev"]
        branch = git["branch"]
        if url.startswith("https://"):
            src_uris.append(f"git://{url[len('https://'):]};protocol=https;branch={branch}")
        elif url.startswith("http://"):
            src_uris.append(f"git://{url[len('http://'):]};protocol=http;branch={branch}")
        elif url.startswith("git@") or url.startswith("ssh://"):
            src_uris.append(f"{url};protocol=ssh;branch={branch}")
        else:
            src_uris.append(f"{url};branch={branch}")
        out.append(f'SRCREV = "{rev}"')
    elif kind == "prebuilt-binary":
        url = m["binary"]["url"]
        if url.startswith("file://"):
            fname = Path(url[len("file://"):]).name
            src_uris.append(f"file://{fname}")
        else:
            src_uris.append(f"{url};sha256sum={m['binary']['sha256']}")
    elif kind == "prebuilt-tarball":
        url = m["binary"]["url"]
        if url.startswith("file://"):
            fname = Path(url[len("file://"):]).name
            src_uris.append(f"file://{fname}")
        else:
            src_uris.append(f"{url};sha256sum={m['binary']['sha256']}")

    # License file must be in SRC_URI so do_populate_lic can find it under WORKDIR.
    if license_ != "CLOSED":
        src_uris.append(f"file://{m['_license_file'].name}")

    # Config and extra files go into SRC_URI so bitbake stages them in WORKDIR.
    for cfg in config:
        src_uris.append(f"file://{Path(cfg['src']).name}")
    for f in files_extra:
        src_uris.append(f"file://{Path(f['src']).name}")
    if service_unit:
        src_uris.append(f"file://{service_unit}")

    out.append('SRC_URI = "{}"'.format(" \\\n           ".join(src_uris)))

    # ----- S ---------------------------------------------------------------
    if kind == "local-source":
        out.append('S = "${WORKDIR}/src"')
    elif kind == "git":
        out.append('S = "${WORKDIR}/git"')
    else:
        out.append('S = "${WORKDIR}"')

    # ----- inherit ---------------------------------------------------------
    inherits = []
    if kind in ("local-source", "git"):
        inh = INHERIT_FOR_BUILD.get(bs, "")
        if inh:
            inherits.append(inh)
    if service_unit:
        inherits.append("systemd")
    if m.get("users"):
        inherits.append("useradd")
    if inherits:
        out.append(f"inherit {' '.join(inherits)}")

    # ----- configure args --------------------------------------------------
    cfg_args = build.get("configure_args") or []
    if cfg_args and bs == "cmake":
        out.append('EXTRA_OECMAKE += "{}"'.format(" ".join(cfg_args)))
    elif cfg_args and bs == "meson":
        out.append('EXTRA_OEMESON += "{}"'.format(" ".join(cfg_args)))
    elif cfg_args and bs == "autotools":
        out.append('EXTRA_OECONF += "{}"'.format(" ".join(cfg_args)))

    # ----- do_compile / do_install -----------------------------------------
    # Strategy:
    #   - cmake/meson/autotools: the inherit class provides both.
    #   - make: emit do_compile via oe_runmake, do_install from install map
    #           or via `make install DESTDIR=...` if no map provided.
    #   - none: empty do_compile; do_install entirely from install map.
    #   - prebuilt-*: empty do_compile; do_install entirely from install map.
    has_install_map = bool(install["bindir"] or install["sbindir"]
                           or install["libdir"] or install["custom"])

    if kind in ("local-source", "git") and bs == "make":
        out.append(textwrap.dedent("""
            do_compile() {
                oe_runmake CC="${CC}" CXX="${CXX}" LD="${LD}" AR="${AR}" \\
                    CFLAGS="${CFLAGS}" CXXFLAGS="${CXXFLAGS}" LDFLAGS="${LDFLAGS}"
            }
        """).strip())
        if not has_install_map:
            out.append(textwrap.dedent("""
                do_install() {
                    oe_runmake DESTDIR="${D}" PREFIX=/usr install
                }
            """).strip())

    if kind in ("local-source", "git") and bs == "none":
        out.append("do_compile() { : ; }")

    if kind in ("prebuilt-binary", "prebuilt-tarball"):
        out.append("do_compile() { : ; }")
        # Binaries are typically pre-stripped; tell QA to allow it.
        out.append('INSANE_SKIP:${PN} += "already-stripped ldflags file-rdeps arch"')
        out.append('INHIBIT_PACKAGE_STRIP = "1"')
        out.append('INHIBIT_SYSROOT_STRIP = "1"')

    # do_install from manifest install map (used for make+map, none, prebuilt-*)
    needs_explicit_install = (
        has_install_map and (
            kind in ("prebuilt-binary", "prebuilt-tarball")
            or (kind in ("local-source", "git") and bs in ("make", "none"))
        )
    )
    if needs_explicit_install:
        block = ["do_install() {"]
        for f in install["bindir"]:
            block.append("    install -d ${D}${bindir}")
            block.append(f'    install -m 0755 ${{S}}/{f} ${{D}}${{bindir}}/')
        for f in install["sbindir"]:
            block.append("    install -d ${D}${sbindir}")
            block.append(f'    install -m 0755 ${{S}}/{f} ${{D}}${{sbindir}}/')
        for f in install["libdir"]:
            block.append("    install -d ${D}${libdir}")
            block.append(f'    install -m 0644 ${{S}}/{f} ${{D}}${{libdir}}/')
        for c in install["custom"]:
            dst_dir = str(Path(c["dst"]).parent)
            block.append(f'    install -d ${{D}}{dst_dir}')
            block.append(f'    install -m {c["mode"]} ${{S}}/{c["src"]} ${{D}}{c["dst"]}')
        block.append("}")
        out.append("\n".join(block))

    # Custom files in FILES:${PN} for non-standard install paths.
    custom_dsts = sorted({c["dst"] for c in install["custom"]})
    if custom_dsts:
        out.append('FILES:${{PN}} += "{}"'.format(" ".join(custom_dsts)))

    # ----- config files install (post-process) -----------------------------
    if config:
        block = ["do_install:append() {"]
        for cfg in config:
            src_name = Path(cfg["src"]).name
            dst_dir = str(Path(cfg["dst"]).parent)
            block.append(f'    install -d ${{D}}{dst_dir}')
            block.append(f'    install -m {cfg["mode"]} ${{WORKDIR}}/{src_name} ${{D}}{cfg["dst"]}')
        block.append("}")
        out.append("\n".join(block))
        cfg_dsts = sorted({cfg["dst"] for cfg in config})
        out.append('CONFFILES:${{PN}} += "{}"'.format(" ".join(cfg_dsts)))
        out.append('FILES:${{PN}} += "{}"'.format(" ".join(cfg_dsts)))

    # ----- extra files install (post-process) ------------------------------
    if files_extra:
        block = ["do_install:append() {"]
        for f in files_extra:
            src_name = Path(f["src"]).name
            dst_dir = str(Path(f["dst"]).parent)
            block.append(f'    install -d ${{D}}{dst_dir}')
            block.append(f'    install -m {f["mode"]} ${{WORKDIR}}/{src_name} ${{D}}{f["dst"]}')
        block.append("}")
        out.append("\n".join(block))
        extra_dsts = sorted({f["dst"] for f in files_extra})
        out.append('FILES:${{PN}} += "{}"'.format(" ".join(extra_dsts)))

    # ----- systemd service -------------------------------------------------
    if service_unit:
        auto = "enable" if service.get("enable", True) else "disable"
        out.append(textwrap.dedent(f"""
            SYSTEMD_PACKAGES = "${{PN}}"
            SYSTEMD_SERVICE:${{PN}} = "{service_unit}"
            SYSTEMD_AUTO_ENABLE:${{PN}} = "{auto}"

            do_install:append() {{
                install -d ${{D}}${{systemd_system_unitdir}}
                install -m 0644 ${{WORKDIR}}/{service_unit} ${{D}}${{systemd_system_unitdir}}/{service_unit}
            }}

            FILES:${{PN}} += "${{systemd_system_unitdir}}/{service_unit}"
        """).strip())

    # ----- users -----------------------------------------------------------
    users = m.get("users") or []
    if users:
        out.append('USERADD_PACKAGES = "${PN}"')
        # Each user is a `--`-separated USERADD_PARAM clause.
        clauses = []
        for u in users:
            uname = u["name"]
            system = "--system" if u.get("system", True) else ""
            shell = u.get("shell", "/sbin/nologin")
            home = u.get("home", "/nonexistent")
            clauses.append(f"-r {system} -d {home} -s {shell} {uname}".strip())
        out.append('USERADD_PARAM:${{PN}} = "{}"'.format(" ; ".join(clauses)))

    return "\n\n".join(out).rstrip() + "\n"


# ------------------------------------------------------------ layer emission -

def emit_layer(apps, layer_dir, default_image):
    layer_dir = Path(layer_dir)
    layer_dir.mkdir(parents=True, exist_ok=True)

    layer_conf = textwrap.dedent("""\
        # AUTO-GENERATED by bin/gen-apps.py
        BBPATH .= ":${LAYERDIR}"
        BBFILES += " \\
            ${LAYERDIR}/recipes-*/*/*.bb \\
            ${LAYERDIR}/recipes-*/*/*.bbappend \\
        "
        BBFILE_COLLECTIONS += "custom-apps"
        BBFILE_PATTERN_custom-apps = "^${LAYERDIR}/"
        BBFILE_PRIORITY_custom-apps = "10"
        LAYERSERIES_COMPAT_custom-apps = "scarthgap"
        LAYERDEPENDS_custom-apps = "core adsp-sc5xx"
    """)
    write_if_changed(layer_dir / "conf" / "layer.conf", layer_conf)

    write_if_changed(
        layer_dir / "README.AUTO-GENERATED",
        "This layer is auto-generated by bin/gen-apps.py from src/apps/.\n"
        "Do NOT edit any file here by hand. Re-run `make apps` after editing app.yaml.\n",
    )

    # Reap orphaned recipe dirs (apps that were deleted from src/apps/).
    recipes_root = layer_dir / "recipes-apps"
    recipes_root.mkdir(parents=True, exist_ok=True)
    current_names = {m["name"] for m, _ in apps}
    for p in recipes_root.iterdir():
        if p.is_dir() and p.name not in current_names:
            info(f"removing orphaned recipe: {p.name}")
            shutil.rmtree(p)

    for m, app_dir in apps:
        emit_recipe(m, app_dir, recipes_root)

    # Packagegroup
    pkg_dir = layer_dir / "recipes-core" / "packagegroups"
    app_names = sorted(m["name"] for m, _ in apps)
    if app_names:
        rdeps = " \\\n".join(f"    {n}" for n in app_names)
        body = (
            GENERATED_HEADER
            + 'SUMMARY = "Custom apps installed by meta-custom-apps"\n'
            + 'LICENSE = "MIT"\n'
            + f'LIC_FILES_CHKSUM = "file://${{COMMON_LICENSE_DIR}}/MIT;md5={MIT_LICENSE_MD5}"\n\n'
            + "inherit packagegroup\n\n"
            + f'RDEPENDS:${{PN}} = " \\\n{rdeps} \\\n"\n'
        )
    else:
        body = (
            GENERATED_HEADER
            + 'SUMMARY = "Custom apps (empty)"\n'
            + 'LICENSE = "MIT"\n'
            + f'LIC_FILES_CHKSUM = "file://${{COMMON_LICENSE_DIR}}/MIT;md5={MIT_LICENSE_MD5}"\n\n'
            + "inherit packagegroup\n\n"
            + 'RDEPENDS:${PN} = ""\n'
        )
    write_if_changed(pkg_dir / "packagegroup-custom-apps.bb", body)

    # Image recipe
    img_dir = layer_dir / "recipes-core" / "images"
    # adsp-sc5xx-minimal-mmc already pulls in wic.gz + ext4 via its inheritance
    # chain; don't add an IMAGE_FSTYPES override here.
    image_body = (
        GENERATED_HEADER
        + "\n"
        + "require recipes-adi/images/adsp-sc5xx-minimal-mmc.bb\n\n"
        + 'SUMMARY = "Custom ADSP-SC5xx image with user apps from src/apps/"\n\n'
        + 'IMAGE_INSTALL:append = " packagegroup-custom-apps"\n'
    )
    write_if_changed(img_dir / f"{default_image}.bb", image_body)


def emit_recipe(m, app_dir, recipes_root):
    name = m["name"]
    version = m["version"]
    recipe_dir = recipes_root / name
    files_dir = recipe_dir / "files"

    # Fully regenerate each recipe directory to avoid stale files.
    if recipe_dir.exists():
        shutil.rmtree(recipe_dir)
    files_dir.mkdir(parents=True, exist_ok=True)

    kind = m["kind"]

    # Stage source / binary
    if kind == "local-source":
        copy_tree(app_dir / "src", files_dir / "src")
    elif kind in ("prebuilt-binary", "prebuilt-tarball"):
        url = m["binary"]["url"]
        if url.startswith("file://"):
            local = app_dir / url[len("file://"):]
            shutil.copy2(local, files_dir / local.name)

    # Stage config, extra files, service unit, license file
    for cfg in (m.get("config") or []):
        shutil.copy2(app_dir / cfg["src"], files_dir / Path(cfg["src"]).name)
    for f in (m.get("files") or []):
        shutil.copy2(app_dir / f["src"], files_dir / Path(f["src"]).name)
    service = m.get("service") or {}
    if service.get("unit"):
        shutil.copy2(app_dir / service["unit"], files_dir / Path(service["unit"]).name)
    if "_license_file" in m:
        shutil.copy2(m["_license_file"], files_dir / m["_license_file"].name)

    bb_path = recipe_dir / f"{name}_{version}.bb"
    bb_path.write_text(render_recipe(m))
    info(f"wrote {bb_path.relative_to(recipes_root.parent.parent)}")


# ----------------------------------------------------------------- subcommands

def cmd_generate(args):
    apps_dir = Path(args.apps_dir).resolve()
    layer_dir = Path(args.layer_dir).resolve()

    if not apps_dir.is_dir():
        die(f"apps dir not found: {apps_dir}")

    apps = []
    for entry in sorted(apps_dir.iterdir()):
        if not entry.is_dir() or entry.name.startswith("."):
            continue
        if not (entry / "app.yaml").is_file():
            info(f"skipping {entry.name} (no app.yaml)")
            continue
        m = load_manifest(entry)
        m = validate(m, entry)
        apps.append((m, entry))

    names = sorted(m["name"] for m, _ in apps)
    if names:
        info(f"found {len(apps)} app(s): {', '.join(names)}")
    else:
        info("no apps configured -- emitting empty layer")
    emit_layer(apps, layer_dir, args.default_image)
    info(f"layer regenerated at {layer_dir}")


def cmd_scaffold(args):
    apps_dir = Path(args.apps_dir).resolve()
    name = args.name
    kind = args.kind

    if not NAME_RE.match(name):
        die(f"app name must match {NAME_RE.pattern!r}")
    if kind not in VALID_KINDS:
        die(f"kind must be one of {VALID_KINDS}")

    # Create the apps dir on demand - e.g. the first `make new-app` on a fresh
    # checkout, where src/apps/ does not exist yet. Explicit (not just an
    # implicit side effect of the app-subdir mkdir below) so it can't regress.
    if not apps_dir.exists():
        info(f"creating apps dir {apps_dir}")
    apps_dir.mkdir(parents=True, exist_ok=True)

    target = apps_dir / name
    if target.exists():
        die(f"{target} already exists")
    target.mkdir()

    if kind == "local-source":
        yaml_body = textwrap.dedent(f"""\
            name: {name}
            version: 0.1.0
            summary: "TODO: describe {name}"
            license: MIT
            kind: local-source
            build:
              system: make
              configure_args: []
            install:
              bindir: [{name}]
            depends: []
            rdepends: []
            # service:
            #   unit: service/{name}.service
            #   enable: true
            # config:
            #   - src: config/{name}.conf
            #     dst: /etc/{name}/{name}.conf
            #     mode: '0644'
        """)
        (target / "src").mkdir()
        (target / "src" / "Makefile").write_text(textwrap.dedent(f"""\
            CC ?= cc
            CFLAGS ?= -O2 -Wall

            {name}: {name}.c
            \t$(CC) $(CFLAGS) $(LDFLAGS) -o $@ $<

            clean:
            \trm -f {name}

            .PHONY: clean
        """))
        (target / "src" / f"{name}.c").write_text(textwrap.dedent(f"""\
            #include <stdio.h>

            int main(void) {{
                printf("Hello from {name}\\n");
                return 0;
            }}
        """))
        # Ignore the in-tree build artifact. The host Makefile compiles
        # <name>.c straight to an extension-less executable named <name>; the
        # top-level .gitignore can't catch that with a glob, so each app
        # carries its own committed ignore for it.
        (target / "src" / ".gitignore").write_text(
            f"# Build output of the in-tree host Makefile (`make` -> ./{name}).\n"
            f"/{name}\n"
        )
        (target / "LICENSE").write_text("MIT License\n\n(replace with full license text)\n")

    elif kind == "git":
        yaml_body = textwrap.dedent(f"""\
            name: {name}
            version: git
            summary: "TODO: describe {name}"
            license: MIT
            kind: git
            git:
              url: https://github.com/example/{name}.git
              rev: 0000000000000000000000000000000000000000
              branch: main
            build:
              system: cmake
              configure_args: []
            depends: []
            rdepends: []
        """)
        (target / "LICENSE").write_text("MIT License\n\n(replace with full license text)\n")

    elif kind == "prebuilt-binary":
        yaml_body = textwrap.dedent(f"""\
            name: {name}
            version: 1.0.0
            summary: "TODO: describe {name}"
            license: CLOSED
            kind: prebuilt-binary
            binary:
              url: "file://binary/{name}"
              # sha256 auto-computed for file:// URLs
            install:
              custom:
                - src: {name}
                  dst: /usr/bin/{name}
                  mode: '0755'
            depends: []
            rdepends: []
        """)
        (target / "binary").mkdir()
        (target / "binary" / "PLACE_BINARY_HERE").write_text(
            f"Drop the ELF named '{name}' in this directory, then run `make apps`.\n"
        )

    elif kind == "prebuilt-tarball":
        yaml_body = textwrap.dedent(f"""\
            name: {name}
            version: 1.0.0
            summary: "TODO: describe {name}"
            license: CLOSED
            kind: prebuilt-tarball
            binary:
              url: "file://binary/{name}.tar.gz"
            install:
              custom:
                - src: bin/{name}
                  dst: /usr/bin/{name}
                  mode: '0755'
            depends: []
            rdepends: []
        """)
        (target / "binary").mkdir()
        (target / "binary" / "PLACE_TARBALL_HERE").write_text(
            f"Drop the tarball named '{name}.tar.gz' here, then run `make apps`.\n"
        )

    (target / "app.yaml").write_text(yaml_body)
    info(f"scaffolded {target}")
    info(f"next: edit {target}/app.yaml, then run `make apps`")


def cmd_list(args):
    apps_dir = Path(args.apps_dir).resolve()
    if not apps_dir.is_dir():
        die(f"apps dir not found: {apps_dir}")
    rows = []
    for entry in sorted(apps_dir.iterdir()):
        if not entry.is_dir() or entry.name.startswith("."):
            continue
        if not (entry / "app.yaml").is_file():
            continue
        try:
            m = load_manifest(entry)
        except SystemExit:
            rows.append((entry.name, "?", "INVALID", ""))
            continue
        rows.append((
            m.get("name", entry.name),
            str(m.get("version", "?")),
            str(m.get("kind", "?")),
            str(m.get("summary", "")),
        ))
    if not rows:
        print("(no apps configured)")
        return
    w = [max(len(r[i]) for r in rows) for i in range(3)]
    for name, ver, kind, summary in rows:
        print(f"  {name:<{w[0]}}  {ver:<{w[1]}}  {kind:<{w[2]}}  {summary}")


